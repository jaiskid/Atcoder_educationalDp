int solve(vector<int>ar, int n) {
	vector<int>dp(n + 1);
	dp[0] = 1;
	for (int i = 2; i <= n; i++) {
		int option1 = abs(ar[i] - ar[i - 1]) + dp[i - 1];
		int option2 = (i == 2) ? inf : abs(ar[i] - ar[i - 2]) + dp[i - 2];
		dp[i] = min(option1, option2);
	}
	return dp[n];
}
