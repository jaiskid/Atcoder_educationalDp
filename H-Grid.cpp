int solve(int n, int m) {
	int  dp[n + 1][m + 1];
	dp[n][m] = 1;
	for (int i = n; i >= 1; i--) {
		for (int j = m; j >= 1; j--) {
			if (i == n and j == m)
				continue;
			if (grid[i][j] == '#')
				dp[i][j] = 0;
			else
				dp[i][j] = (((i == n) ? 0 : dp[i + 1][j]) + ((j == m) ? 0 : dp[i][j + 1]))%mod;
		}
	}
	return dp[1][1];
}
