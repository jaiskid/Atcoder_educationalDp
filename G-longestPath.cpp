vector<int>graph[100001];
int dp[100001]; //dp[i] =len of longest path starting at node i
int solve(int src) {
	if (dp[src] != -1) {
		return dp[src];
	}
	bool leaf = 1;
	int bestChild = 0;
	for (int child : graph[src]) {
		leaf = 0;
		bestChild = max(solve(child), bestChild);
	}
	return dp[src] = leaf ? 0 : 1 + bestChild;
}
